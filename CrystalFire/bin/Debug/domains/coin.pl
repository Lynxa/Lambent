% =========================================================================================================================
% 									Domain Specification
% =========================================================================================================================




% =====================================================================
% Class Specification
% =====================================================================

classes([

	(coin, [
		owned_by, 
		held_by, 
		side_up
	])

]).


% =====================================================================
% Initial Specification
% =====================================================================

initially([

	create_object(coin(1), coin)

]).


% =====================================================================
% On Entrance Specification
% =====================================================================

on_entrance(Agent, [

	issue(right(flip_coin(Coin), (
			object(Coin),
			value(Coin, instance_of, coin)
		)), Agent)

]).



% =====================================================================
% On Departure Specification
% =====================================================================

on_departure(Agent, []).


% =====================================================================
% Action Specification
% =====================================================================

action(Agent, flip_coin(Coin)) :-

	preconditions([
		object(Coin),
		value(Coin, instance_of, coin)
	]),

	effects([
		set(Coin, side_up, Side)
		where choose(Side, [(0.5, heads), (0.5, tails)])
	]).

